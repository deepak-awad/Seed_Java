package Day4_4;

/*** Addition of array or Sum of array ***/

public class TwoDimensionalArray {

	public static void main(String[] args) {
		
		int num[][]={{10,20},{30,40}};
		for(int i =0;i<num.length;i++)
		{
			for(int j=0;j<num.length;j++)
			{
				System.out.println(""+num[i][j]);
			}
		}

	}

}
