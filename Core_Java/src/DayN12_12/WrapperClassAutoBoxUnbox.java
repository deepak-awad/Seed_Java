package DayN12_12;

/*** Wrapper Class autoboxing and unboxing Example  ***/

public class WrapperClassAutoBoxUnbox {

	public static void main(String[] args) {
		
		int x = 10;
		Integer b = x;// autoboxing 
		System.out.println(b);
		
		int c = b.intValue();//autounboxing
		System.out.println(c);
		
		
		
		

	}

}
